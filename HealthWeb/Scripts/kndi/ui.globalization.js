function loadCultureFiles(name) {
    var files = ['ca-gregorian.json', 'numbers.json', 'timeZoneNames.json', 'currencies.json', 'dateFields.json'];
    var loadCulture = function (prop) {
        var val, ajax;
        ajax = new ej.base.Ajax(location.origin + '/Scripts/cldr-data/main/' + name + '/' + files[prop], 'GET', false);
        ajax.onSuccess = function (value) {
            val = value;
        };
        ajax.send();
        ej.base.loadCldr(JSON.parse(val));
        if (name === 'zh-tw')
            ej.base.setCulture('zh-Hant'); //set culture file globally 
        else
            ej.base.setCulture(name); //set culture file globally 
        //ej.base.setCulture('zh-Hant'); //set culture file globally
        //ej.base.setCurrencyCode('NTD'); //set currency code globally 
    };
    for (var prop = 0; prop < files.length; prop++) {
        loadCulture(prop);
    }
}

